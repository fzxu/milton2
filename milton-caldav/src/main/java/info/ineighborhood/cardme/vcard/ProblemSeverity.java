public enum ProblemSeverity {
	
	/**
	 * <p>An error resulting in exceptions being thrown
	 * and the occurrence of invalid data. Anything that
	 * will make the VCard unusable in all circumstances.</p>
	 */
	ERROR,
	
	/**
	 * <p>Errors resulting in exceptions being thrown that
	 * indicate invalid or malformed data. In this case the
	 * vcard is somewhat valid. May work with some applications
	 * and may fail on others. Any features that contained
	 * invalid data will most likely be omitted from the vcard.</p>
	 */
	WARNING,
	
	/**
	 * TODO
	 */
	HINT,
	
	/**
	 * <p>No errors occurred.</p>
	 */
	NONE
}
