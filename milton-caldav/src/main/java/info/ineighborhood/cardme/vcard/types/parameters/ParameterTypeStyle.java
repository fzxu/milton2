/**
 * 
 * @author George El-Haddad
 * <br/>
 * Feb 10, 2010
 * 
 * <p>This specifies if we will use a type value list
 * or a type parameter list when writing out the types
 * of a specified feature. Note that both mean the same
 * thing, it is just how it is written.</p>
 */
public enum ParameterTypeStyle {

	/**
	 * <p>Formats the parameter types as a list of parameters
	 * <ul>
	 * 	<li><code>TYPE=dom;TYPE=postal;TYPE=home;TYPE=WORK</code></li>
	 * </ul>
	 * </p>
	 */
	PARAMETER_LIST,
	
	/**
	 * <p>Formats the parameter types as a value list.
	 * <ul>
	 * 	<li><code>TYPE=dom,postal,home,work</code></li>
	 * </ul>
	 * </p>
	 */
	PARAMETER_VALUE_LIST;
}
